// !n = n * !n - 1

double Factorial(double n)
{
    if(n == 1) return 1;
    else return n * Factorial(n - 1);
}

for(int i = 1; i < 40; i++)
{
    System.Console.WriteLine($"{i}! = {Factorial(i)}");
}

// f(1) = 1
// f(2) = 1
// f(n) = f(n - 1) + f(n - 2)

double Fibonacci(double n)
{
    if(n == 1 || n == 2) return 1;
    else return n = Fibonacci(n - 1) + Fibonacci(n - 2);
}

for(int i = 1; i < 30; i++)
{
    System.Console.WriteLine($"f{i} = {Fibonacci(i)}");
}

